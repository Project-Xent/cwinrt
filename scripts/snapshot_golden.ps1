# Capture golden snapshots of the generated surface, so CI can prove
# regeneration on the pinned SDK is byte-stable and flag any ABI change.
#
# Produces, under tests/golden/:
#   files.sha256  - SHA-256 of every generated header + impl (sorted by path)
#   iids.txt      - canonical interface IIDs (cwinrt-gen --dump-iids), sorted
#   piids.txt     - parameterized IIDs for generic instantiations, sorted
#   symbols.txt   - the exported C function surface (HRESULT decls), sorted
#
# The pinned SDK version lives in sdk.version (single source of truth).
param([switch]$Check)

$ErrorActionPreference = "Stop"
$root    = Split-Path -Parent $PSScriptRoot
$sdk     = (Get-Content (Join-Path $root "sdk.version") -Raw).Trim()
$kits    = "${env:ProgramFiles(x86)}\Windows Kits\10"
$winmd   = Join-Path $kits "UnionMetadata\$sdk\Windows.winmd"
$gen     = Join-Path $root "build\windows\x64\release\cwinrt-gen.exe"
$inc     = Join-Path $root "include\cwinrt"
$golden  = Join-Path $root "tests\golden"
$dest    = if ($Check) { Join-Path ([System.IO.Path]::GetTempPath()) "cwinrt_golden_check" } else { $golden }

if (-not (Test-Path $winmd)) { Write-Error "Union metadata not found: $winmd" }
if (-not (Test-Path $gen))   { Write-Error "cwinrt-gen not built" }
New-Item -ItemType Directory -Force $dest | Out-Null

# 1. Per-file SHA-256 of every generated header + impl, sorted by repo-relative path.
$files = Get-ChildItem -Recurse -File (Join-Path $inc "*") | Where-Object { $_.Extension -in ".h", ".c" }
$lines = foreach ($f in $files) {
    $rel = $f.FullName.Substring($root.Length + 1) -replace '\\', '/'
    "{0}  {1}" -f (Get-FileHash $f.FullName -Algorithm SHA256).Hash.ToLower(), $rel
}
($lines | Sort-Object) -join "`n" | Set-Content (Join-Path $dest "files.sha256") -NoNewline

# 2/3. IID + PIID tables (canonical GUID surface), sorted.
(& $gen --dump-iids  $winmd | Sort-Object) -join "`n" | Set-Content (Join-Path $dest "iids.txt")  -NoNewline
(& $gen --dump-piids $winmd | Sort-Object) -join "`n" | Set-Content (Join-Path $dest "piids.txt") -NoNewline

# 4. Exported C function surface: every "HRESULT name(...)" declaration, sorted.
$decls = Select-String -Path (Join-Path $inc "*.h") -Pattern '^HRESULT [a-z0-9_]+\(' |
    ForEach-Object { $_.Line.Trim() }
($decls | Sort-Object -Unique) -join "`n" | Set-Content (Join-Path $dest "symbols.txt") -NoNewline

if ($Check) {
    $bad = 0

    # files.sha256 + symbols.txt are the real determinism / ABI-surface gates: the
    # generated bytes and the exported C functions must match exactly. Compare by
    # content hash (O(n)); Compare-Object is too slow on the large tables.
    foreach ($name in "files.sha256", "symbols.txt") {
        $ref = Join-Path $golden $name
        $cur = Join-Path $dest $name
        if (-not (Test-Path $ref)) { Write-Host "MISSING golden $name (run snapshot_golden.ps1)"; $bad++; continue }
        $hr = (Get-FileHash $ref -Algorithm SHA256).Hash
        $hc = (Get-FileHash $cur -Algorithm SHA256).Hash
        if ($hr -ne $hc) {
            Write-Host "GOLDEN DRIFT in $name"
            # Diagnose: same line set => EOL/order only (e.g. CRLF); else show the
            # actual content lines that differ, so a CI failure is self-explanatory.
            $refLines = [System.IO.File]::ReadAllLines($ref)
            $curLines = [System.IO.File]::ReadAllLines($cur)
            $refSet = [System.Collections.Generic.HashSet[string]]::new([string[]]$refLines)
            $curSet = [System.Collections.Generic.HashSet[string]]::new([string[]]$curLines)
            if ($refSet.SetEquals($curSet)) {
                Write-Host "  (identical line set -- difference is EOL/order only, e.g. CRLF vs LF)"
            } else {
                $onlyRef = @($refLines | Where-Object { -not $curSet.Contains($_) })
                $onlyCur = @($curLines | Where-Object { -not $refSet.Contains($_) })
                Write-Host ("  {0} line(s) only in golden, {1} line(s) only in CI output:" -f $onlyRef.Count, $onlyCur.Count)
                $onlyRef | Select-Object -First 10 | ForEach-Object { Write-Host "    -golden: $_" }
                $onlyCur | Select-Object -First 10 | ForEach-Object { Write-Host "    +ci:     $_" }
            }
            $bad++
        }
        else { Write-Host "ok $name" }
    }

    # iids.txt / piids.txt are "<name> <GUID>" tables. Their *set* of entries is a
    # function of the SDK winmd, which is NOT pinned by version string -- servicing
    # rewrites Windows.winmd in place under the same 10.0.NNNNN.0 path -- so two
    # machines legitimately enumerate different interface sets. The gate's real job
    # is catching a GUID-EXTRACTION regression (a wrong GUID on an interface that
    # exists on both sides). So: FAIL only when a shared name's GUID differs; a name
    # present on just one side's winmd is an SDK-content difference and is tolerated
    # (warned). files.sha256/symbols.txt above still catch any functional-surface change.
    foreach ($name in "iids.txt", "piids.txt") {
        $ref = Join-Path $golden $name
        $cur = Join-Path $dest $name
        if (-not (Test-Path $ref)) { Write-Host "MISSING golden $name (run snapshot_golden.ps1)"; $bad++; continue }
        $refMap = @{}; foreach ($l in [System.IO.File]::ReadAllLines($ref)) { if ($l) { $i = $l.LastIndexOf(' '); $refMap[$l.Substring(0, $i)] = $l.Substring($i + 1) } }
        $curMap = @{}; foreach ($l in [System.IO.File]::ReadAllLines($cur)) { if ($l) { $i = $l.LastIndexOf(' '); $curMap[$l.Substring(0, $i)] = $l.Substring($i + 1) } }
        $mismatch = @($refMap.Keys | Where-Object { $curMap.ContainsKey($_) -and $curMap[$_] -ne $refMap[$_] })
        $onlyRef  = @($refMap.Keys | Where-Object { -not $curMap.ContainsKey($_) })
        $onlyCur  = @($curMap.Keys | Where-Object { -not $refMap.ContainsKey($_) })
        if ($mismatch.Count) {
            Write-Host "GOLDEN DRIFT in ${name}: $($mismatch.Count) GUID(s) changed on shared entries (extraction/ABI regression):"
            $mismatch | Select-Object -First 10 | ForEach-Object { Write-Host ("    {0}: golden={1} ci={2}" -f $_, $refMap[$_], $curMap[$_]) }
            $bad++
        }
        elseif ($onlyRef.Count -or $onlyCur.Count) {
            $shared = $refMap.Count - $onlyRef.Count
            Write-Host ("warn ${name}: GUIDs agree on all $shared shared entries; set differs by SDK winmd (tolerated): $($onlyRef.Count) only in golden, $($onlyCur.Count) only in CI")
            $onlyRef | Select-Object -First 5 | ForEach-Object { Write-Host "    -golden: $_" }
            $onlyCur | Select-Object -First 5 | ForEach-Object { Write-Host "    +ci:     $_" }
        }
        else { Write-Host "ok $name" }
    }

    Remove-Item -Recurse -Force $dest
    if ($bad) { Write-Error "golden check FAILED ($bad files drifted) — ABI/output changed; if intended, bump sdk.version/ABI version and re-run snapshot_golden.ps1" }
    Write-Host "golden check: bytes + ABI surface exact; GUID values verified on every shared interface"
}
else {
    $n = (Get-Content (Join-Path $dest "symbols.txt")).Count
    Write-Host "wrote golden snapshots to $dest ($($files.Count) files, $n exported functions)"
}
